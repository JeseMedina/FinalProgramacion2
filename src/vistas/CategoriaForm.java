    void listar(List<Categoria> lista) {
        modelo = (DefaultTableModel) tabla.getModel();
        Object[] ob = new Object[2];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getId();
            ob[1] = lista.get(i).getNombre();
            modelo.addRow(ob);
        }
        tabla.setModel(modelo);
    }

    void filtrar() {
        if ("ID".equals(txtFiltrar.getSelectedItem().toString())) {
            List<Categoria> lista = dao.listarId(Integer.parseInt(txtBuscar.getText()));
            listar(lista);
        } else {
            List<Categoria> lista = dao.listarNombre(txtBuscar.getText());
            listar(lista);
        }
    }

