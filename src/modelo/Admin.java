public class Admin {

    int id;
    String user;
    String contrasena;

    public Admin() {
    }

    public Admin(int id, String user, String contrasena) {
        this.id = id;
        this.user = user;
        this.contrasena = contrasena;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }

    
}
